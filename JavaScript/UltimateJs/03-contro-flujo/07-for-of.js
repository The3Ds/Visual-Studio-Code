// iterar un listado de elementos
let animales = ['Chanchito feliz', 'Dragón', 'Perrito'];

for (let animal of animales) {
    console.log(animal);
}

// iterar un listado de elementos, pero más complicado
let i = 0;
while (i < animales.length) {
    console.log(animales[i]);
    i++;
}