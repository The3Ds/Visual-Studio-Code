let a = 10;

// Relacionales
console.log(a > 5);
console.log(a >= 5);
console.log(a < 5);
console.log(a <= 5);

// Igualdad
console.log(a == 10);
console.log(a != 10);
console.log(a == '10'); //evalua si son iguales, sin importar el tipo
console.log(a != '10'); 
console.log(a === '10'); //evalua la igualdad y el tipo
console.log(a !== '10');